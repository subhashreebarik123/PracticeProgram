package com.java.rest.restwebservicehello.print.JavaControlStatement.LoopStatement;

public class ForLoopExample1 {
    public static void main(String[] args) {
        int sum =0;
        for(int i=1;i<=10;i++){
            sum = sum+i;
            System.out.println("The first 10 natural number :" + sum);
        }

    }
}
