package com.java.rest.restwebservicehello.print.JavaControlStatement.IfElseStatement;

public class IfElseIfStudent {
    public static void main(String[] args) {
        String city = "Berhampur";
        if(city == "Cuttack"){
            System.out.println("City is Cuttack");
        } else if (city == "Jajpur") {
            System.out.println("City is Jajpur");
        } else if (city =="Berhampur"){
            System.out.println("Berhampur");
        } else if (city == "Puri") {
            System.out.println("Puri");

        }else {
            System.out.println(city);
        }

    }
}
