package com.java.rest.restwebservicehello.print.JavaControlStatement.SwitchStatement;

public class SwitchExample3 {
    public static void main(String[] args) {
        char ch = 'o';
        switch (ch) {
            case 'a':
                System.out.println("Vowel");
                break;
            case 'e':
                System.out.println("Vowel");
                break;
            case 'i':
                System.out.println("Vowel");
                break;
            case 'o':
                System.out.println("Vowel");
                break;
            case 'u' :
                System.out.println("Vowel");
            default:
                System.out.println("Constant");
            }
        }
    }

